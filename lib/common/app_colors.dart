import 'package:flutter/material.dart';

/// > This class contains static methods that return a color
class AppColors {
  static Color get bgBlue => const Color(0XFF000C66);

  static Color get shadowColor => const Color(0XFF2D74FF);

  static Color get black => const Color(0X00000000);

  static Color get bgColor => const Color(0XFFE0DEDE);

  static Color get primary => const Color.fromRGBO(32, 38, 46, 1.0);

  static Color get secondary => const Color.fromRGBO(145, 49, 117, 1.0);
}
